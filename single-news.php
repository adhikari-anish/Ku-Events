            <p class="text-justify">Design is in everything you see. Most of us relate design to just fashion or posters and flyers and web designs and other electronics stuffs, but that is only a small part in the world of designing. The pen you write with is designed to fit perfectly between your fingers. The clothes you wear in the winter are made so that they allow less heat to leave your body; the clothes in the summer function opposite. Even the chair you sit on may be more or less comfortable depending on its design. But the best designer in the world is Mother Nature. Surely you’ve noticed how beautiful the flowers are and admired the snow-sprinkled mountains. So you see design is actually in everything, big or small.
But funny thing is I didn’t know any of this a year ago. You never know what is in store for you until you do figure it out. When you do, it opens up a whole new world of opportunities. Until a year ago, I was like any of you at design, barely knowing how to use Adobe Photoshop and its tools. Then IT Meet 2016 happened. Me and my friend, Ishan, were almost dragged into the Design Challenge from our Project Demonstration stalls. There we were asked to design a mockup for a company’s site. Ishan had neither Photoshop nor Illustrator in his laptop, so he used mine. Meanwhile, I barely managed to install Illustrator on his. I would have preferred Photoshop but for some reason Ishan’s laptop refused to install it. Still baffled by our utter defeat in the Counter Strike Gaming event, I did what I could within the last hour of the challenge. We all handed in our designs for presentation. I stayed in for a while as the people before me presented their designs. And as I watched, I became more and more frantic. I became sure that my design wouldn’t compare to any of the others’. So I pretended to get a phone call and bailed. Yes, I was that afraid of presentations!
Now, what would guess I ranked in the challenge? If you think I topped the list, you’re …wrong! That would have been a predictable story – a winner writing this post. No, this isn’t the story of a winner. Nor a defeat. I ranked fourth. And Ishan ranked second. Not a very good thing to brag about is it? Well it’s more about what the event brought about than the event itself.
When my friend told me that I ranked fourth I was shocked. I hadn’t the nerve to present and despite that I somehow managed to beat all those below me in the rankings. You see, design isn’t about what tool you know. It’s about seeing the way things could be presented beautifully. Design isn’t making it look pretty, it’s about finding a solution.
That inspired me to dig deeper in design. I sort of volunteered to design posters, flyers and banners for several events in the university. Again, I say ‘sort of’ because I didn’t ask for any of those. I just didn’t say no. One day, a friend calls me and asks ‘How would you like to design a flyer for the World Environment Day?’. I reply, just waking up from a slumber, ‘Umm, sure. If it’s just one flyer I don’t see the problem.’ It turned out to be a series of flyers and banners instead of just one flyer. And that was the first of the flight of stairs that led me to where I am, to where we are.
Today, Ishan and I are designers for IT Express 2017. Neither of us knew how to use Adobe InDesign, which is the software used for print design. We learned however. It was a struggle and quite an experience. Our fruits are due to be published Friday, 20th January, 2017 – which is tomorrow, on the date that I’m writing this. And we’re coordinators to the event that led us to this path. Now, we’re urging you to follow the same. You never know what doors it may open for you.
So I call out to all the designers out there. Even those who are inclined slightly to the concept. If you don’t know how to use Adobe Photoshop or other tools, don’t worry. We’ll be there to guide you through the way. It’s not that daunting to learn. As I said, it’s not the tools you use; it’s just seeing things the way they could be.
Design Challenge will be held on Saturday, 21st January, 2017 as part of IT Meet 2017. Hoping to see to more designers there.</p>
