           <p class="text-justify" style="color:#000">KU Events is a user-friendly web application designed to let the students as well as staffs to stay up-to-date on the upcoming events that are to be held in KU premises. Not only can they stay updated but also can participate, show interests and interact with the event coordinators and various clubs that are involved in conducting those events.
 <br><br>
Objectives <br>
The main objectives of our project are: <br>
•   To create a platform where the users can remain updated on various events in account of their respective departments or personal interests. <br>
•   To let the users show interests and participate in those respective events. <br>
•   To spread the knowledge about all the clubs and associations present in KU premises and their works. <br>
•   To make the events organized by each clubs and associations a remarkable success. <br>
•   To gain knowledge and experience on web development. <br>


 <br><br>
Significance and scopes <br>
Today, the world has come to an age where everyone is practically dependent on the internet. Owing to this mounting magnitude of dependency, the significance of this project are mentioned below: <br>
•   With the help of this site, each and every site viewer can get updated to upcoming academic and non-academic events. <br>
•   Since this site gives detailed information about various events of every KU clubs and departments, concerned viewer don’t have to bother to surf multiple websites and pages of every clubs and departments. <br>
•   This site can aid event organizers to run their program more effectively by promoting participation of audience in their events. <br>


